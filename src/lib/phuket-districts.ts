type LngLat = [number, number]

type BBoxDeg = { minLng: number; maxLng: number; minLat: number; maxLat: number }

function bboxDegreesFromFeatures(
  features: Array<{ geometry: { type: string; coordinates: number[][][] | number[][][][] } }>
): BBoxDeg {
  let minLng = Infinity
  let maxLng = -Infinity
  let minLat = Infinity
  let maxLat = -Infinity
  for (const f of features) {
    const g = f.geometry
    if (!g || g.type !== 'Polygon') continue
    const rings = g.coordinates as number[][][]
    for (const ring of rings) {
      for (const pt of ring) {
        const lng = Number(pt[0])
        const lat = Number(pt[1])
        if (!Number.isFinite(lng) || !Number.isFinite(lat)) continue
        minLng = Math.min(minLng, lng)
        maxLng = Math.max(maxLng, lng)
        minLat = Math.min(minLat, lat)
        maxLat = Math.max(maxLat, lat)
      }
    }
  }
  const padLng = Math.max(1e-6, (maxLng - minLng) * 0.02)
  const padLat = Math.max(1e-6, (maxLat - minLat) * 0.02)
  return {
    minLng: minLng - padLng,
    maxLng: maxLng + padLng,
    minLat: minLat - padLat,
    maxLat: maxLat + padLat,
  }
}

function projectLngLat(b: BBoxDeg, lng: number, lat: number): LngLat {
  const { width, height, padding } = SVG_VIEW
  const sx = (width - 2 * padding) / (b.maxLng - b.minLng)
  const sy = (height - 2 * padding) / (b.maxLat - b.minLat)
  const x = padding + (lng - b.minLng) * sx
  const y = padding + (b.maxLat - lat) * sy
  return [x, y]
}

/** Planar ring centroid in lng/lat (exterior ring only; fine for small admin polygons). */
function ringCentroidLngLat(ring: LngLat[]): LngLat {
  const core = ring.length > 1 && ring[0][0] === ring[ring.length - 1][0] && ring[0][1] === ring[ring.length - 1][1] ? ring.slice(0, -1) : ring.slice()
  if (core.length < 3) return ring[0] ?? [0, 0]
  let twice = 0
  let cx = 0
  let cy = 0
  const n = core.length
  for (let i = 0, j = n - 1; i < n; j = i++) {
    const [x0, y0] = core[j]
    const [x1, y1] = core[i]
    const cross = x0 * y1 - x1 * y0
    twice += cross
    cx += (x0 + x1) * cross
    cy += (y0 + y1) * cross
  }
  if (Math.abs(twice) < 1e-14) return core[0]
  return [cx / (3 * twice), cy / (3 * twice)]
}

function exteriorRingToSvgPath(b: BBoxDeg, ring: number[][]): string {
  const closed =
    ring.length > 1 &&
    ring[0][0] === ring[ring.length - 1][0] &&
    ring[0][1] === ring[ring.length - 1][1]
  const pts = (closed ? ring.slice(0, -1) : ring.slice()).map(([lng, lat]) => projectLngLat(b, Number(lng), Number(lat)))
  if (pts.length < 2) return ''
  let d = `M${pts[0][0]},${pts[0][1]}`
  for (let i = 1; i < pts.length; i++) d += `L${pts[i][0]},${pts[i][1]}`
  d += 'Z'
  return d
}

function buildPhuketDistrictSvgMeta(): Record<string, PhuketDistrictSvgMeta> {
  const features = JSON.parse(JSON.stringify(PHUKET_DISTRICTS_GEOJSON.features)) as Array<{
    type: string
    id: string
    properties: { name: string }
    geometry: { type: string; coordinates: number[][][] }
  }>
  const b = bboxDegreesFromFeatures(features)
  const out: Record<string, PhuketDistrictSvgMeta> = {}
  for (const f of features) {
    const id = String(f.id)
    const geom = f.geometry
    if (!geom || geom.type !== 'Polygon' || !Array.isArray(geom.coordinates?.[0])) continue
    const exterior = geom.coordinates[0] as number[][]
    const d = exteriorRingToSvgPath(b, exterior)
    if (!d) continue
    const [lngC, latC] = ringCentroidLngLat(exterior as LngLat[])
    const [labelX, labelY] = projectLngLat(b, lngC, latC)
    const ov = PHUKET_DISTRICT_SVG_LABEL_OVERRIDES[id]
    out[id] = { d, labelX, labelY, ...ov }
  }
  return out
}

